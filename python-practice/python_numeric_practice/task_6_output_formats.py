number = float(input("Введите число: "))

# Вывод через запятую
print("Вы ввели число:", number)

# Вывод через f-строку
print(f"Число округлённое до 2 знаков: {number:.2f}")

# Форматированный вывод через format()
print("Форматированный вывод: {:.3f}".format(number))

# Подсказка для пользователя
print(f"Ваше число ({number}) успешно обработано.")
